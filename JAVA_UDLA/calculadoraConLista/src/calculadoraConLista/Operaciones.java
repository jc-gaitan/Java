
package ventana13;

public class Operaciones {
      public Double Sumar(double n1, double n2){
        return n1+n2;
    }
    
    public Double Restar(double n1, double n2){
        return n1-n2;
    }
    
    public Double Dividir(double n1, double n2){
        return n1/n2;
    }
    
    public Double Multiplicar(double n1, double n2){
        return n1*n2;
    }
}
