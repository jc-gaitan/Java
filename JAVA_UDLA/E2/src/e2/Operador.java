
package e2;

public class Operador {
    
    public int sumar(int num,int num2){
        return num+num2;
    }
    
    public int restar(int num,int num2){
        return num-num2;
    }
    
    public int multiplicar(int num,int num2){
        return num*num2;
    }
    
    public float dividir(int num,int num2){
        return (float) num/num2;
    }
    
}
